import collections

RUNE_MAP = {
    'ᚠ': 0, 'ᚢ': 1, 'ᚦ': 2, 'ᚩ': 3, 'ᚱ': 4, 'ᚳ': 5, 'ᚷ': 6, 'ᚹ': 7, 
    'ᚻ': 8, 'ᚾ': 9, 'ᛁ': 10, 'ᛄ': 11, 'ᛇ': 12, 'ᛈ': 13, 'ᛉ': 14, 'ᛋ': 15, 
    'ᛏ': 16, 'ᛒ': 17, 'ᛖ': 18, 'ᛗ': 19, 'ᛚ': 20, 'ᛝ': 21, 'ᛟ': 22, 'ᛞ': 23, 
    'ᚪ': 24, 'ᚫ': 25, 'ᚣ': 26, 'ᛡ': 27, 'ᛠ': 28
}
REVERSE_MAP = {v: k for k, v in RUNE_MAP.items()}

def load_runes(file_path):
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read().replace('\n', '').replace(' ', '').replace('•', '')
    return [c for c in content if c in RUNE_MAP]

p20_runes = load_runes('LiberPrimus/pages/page_20/runes.txt')
counts = collections.Counter(p20_runes)
total = len(p20_runes)

print(f"Total: {total}")
for r, count in counts.most_common():
    idx = RUNE_MAP[r]
    pct = (count / total) * 100
    print(f"{r} ({idx}): {count} ({pct:.2f}%)")
